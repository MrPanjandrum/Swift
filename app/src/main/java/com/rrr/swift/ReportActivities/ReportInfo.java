package com.rrr.swift.ReportActivities;

public class ReportInfo
{

    private long taskStarted;
    private long taskStopped;
    private long taskFinished;

    public ReportInfo(){}


    public long getTaskStarted() { return taskStarted; }

    public void setTaskStarted(long taskStarted) { this.taskStarted = taskStarted; }

    public long getTaskStopped() { return taskStopped; }

    public void setTaskStopped(long taskStopped) { this.taskStopped = taskStopped; }

    public long getTaskFinished() { return taskFinished; }

    public void setTaskFinished(long taskFinished) { this.taskFinished = taskFinished; }


}
